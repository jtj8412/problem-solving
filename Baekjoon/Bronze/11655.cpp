#include <iostream>

int main() {
    char s[102];

    fgets(s, 102, stdin);

    for (int i = 0; s[i] != '\0'; ++i) {
        if (s[i] <= '9') continue;
        if (s[i] >= 'a') s[i] = (s[i] + 13 > 'z') ? 'a' + (s[i] + 12 - 'z') : s[i] + 13;
        else s[i] = (s[i] + 13 > 'Z') ? 'A' + (s[i] + 12 - 'Z') : s[i] + 13;
    }

    printf("%s", s);
}