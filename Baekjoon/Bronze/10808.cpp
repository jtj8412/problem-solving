#include <iostream>

int main() {
    int arr[26] = {}, i;
    char s[101];

    // input
    scanf("%s", s);

    // logic
    for (i = 0; s[i] != '\0'; ++i) ++arr[s[i] - 'a'];

    for (i = 0; i < 26; ++i) printf("%d ", arr[i]);
}