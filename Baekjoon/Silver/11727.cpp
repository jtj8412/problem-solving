// [ 2xn 타일링 2 ] https://www.acmicpc.net/problem/11727
//----------------------------------------------------------------------------------

#include <iostream>

int main() {
    int dp[1001] = {0, 1, 3}, n, i;

    // init
    scanf("%d", &n);

    // logic
    for (i = 3; i <= n; ++i) dp[i] = (dp[i-1] + dp[i-2] * 2) % 10007;

    printf("%d", dp[n]);
}