// [ 1, 2, 3 더하기 ] https://www.acmicpc.net/problem/9095
//----------------------------------------------------------------------------------

#include <iostream>

int main() {
    int n, tc, i, dp[11] = {0, 1, 2, 4};

    // init
    scanf("%d", &n);

    // logic
    for (i = 4; i < 11; ++i) dp[i] = dp[i-1] + dp[i-2] + dp[i-3];

    for (i = 0; i < n; ++i) {
        scanf("%d", &tc);
        printf("%d\n", dp[tc]);
    }
}